def soma():
    x = float(input("Digite o primeiro número: "))
    y = float(input("Digite o segundo número: "))
    print(f"Soma: {x + y}")



def subtracao():
    x = float(input("Digite o primeiro número: "))
    y = float(input("Digite o segundo número: "))
    print(f"Subtração: {x - y}")

soma()
subtracao()




